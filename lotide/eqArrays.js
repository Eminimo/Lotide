const eqArrays = function(arr1, arr2) {
  // Compare lengths of both arrays
  if (arr1.length !== arr2.length) {
    return false;
  }

  // Compare elements of the arrays
  for (let i = 0; i < arr1.length; i++) {
    if (arr1[i] !== arr2[i]) {
      return false;
    }
  }

  return true;
};

